CREATE OR REPLACE FUNCTION 
    LANGUAGE plpgsql IMMUTABLE
    AS $_$
BEGIN
 IF ($1=NULL) THEN
  RETURN 0;
 END IF;

 RETURN $1;
END;$_$;


